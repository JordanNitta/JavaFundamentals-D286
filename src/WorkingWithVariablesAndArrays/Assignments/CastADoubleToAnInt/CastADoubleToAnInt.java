package WorkingWithVariablesAndArrays.Assignments.CastADoubleToAnInt;

/*
1. Create a new class for this exercise, add a main method.

2. Declare a double variable number.

3. Assign a value to number.

4. Cast the double to an int

5. Print the result.
 */
public class CastADoubleToAnInt {
    public static void main(String[] args) {
        double number = 5.0;
        int doubleNum = (int) number;
        System.out.println(doubleNum);
    }
}
